// var : el valor se puede modificar dentro del programa, su alcance es global
// No recomendado su uso 
var b = 7
console.log('var',b)

// const : el valor no se puede modificar dentro del programa, su alcance es local
const a = 5
console.log("const:",a)

// let : el valor se puede modificar dentro del programa, su alcance es local
let c = 5
console.log('let',c)    